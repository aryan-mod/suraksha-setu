const LanguageContext = createContext<LanguageContextType | undefined>(undefined)

export function LanguageProvider({ children }: { children: ReactNode }) {
  const [currentLanguage, setCurrentLanguage] = useState("en")

  const availableLanguages = [
    { code: "en", name: "English", flag: "🇺🇸", rtl: false },
    { code: "hi", name: "हिंदी", flag: "🇮🇳", rtl: false },
    { code: "es", name: "Español", flag: "🇪🇸", rtl: false },
    { code: "fr", name: "Français", flag: "🇫🇷", rtl: false },
    { code: "de", name: "Deutsch", flag: "🇩🇪", rtl: false },
    { code: "pt", name: "Português", flag: "🇵🇹", rtl: false },
    { code: "it", name: "Italiano", flag: "🇮🇹", rtl: false },
    { code: "ja", name: "日本語", flag: "🇯🇵", rtl: false },
    { code: "ko", name: "한국어", flag: "🇰🇷", rtl: false },
    { code: "zh", name: "中文", flag: "🇨🇳", rtl: false },
    { code: "ar", name: "العربية", flag: "🇸🇦", rtl: true },
    { code: "ru", name: "Русский", flag: "🇷🇺", rtl: false },
  ]

  const setLanguage = (lang: string) => {
    setCurrentLanguage(lang)
    localStorage.setItem("safetour-language", lang)

    const selectedLang = availableLanguages.find((l) => l.code === lang)
    if (selectedLang?.rtl) {
      document.documentElement.dir = "rtl"
      document.documentElement.lang = lang
    } else {
      document.documentElement.dir = "ltr"
      document.documentElement.lang = lang
    }
  }

  const translate = (key: string): string => {
    const langTranslations = translations[currentLanguage as keyof typeof translations]
    const englishTranslations = translations.en

    // First try current language, then fallback to English, then return key
    return (
      langTranslations?.[key as keyof typeof langTranslations] ||
      englishTranslations[key as keyof typeof englishTranslations] ||
      key
    )
  }

  const formatNumber = (num: number): string => {
    try {
      return new Intl.NumberFormat(getLocaleFromLanguage(currentLanguage)).format(num)
    } catch {
      return num.toString()
    }
  }

  const formatDate = (date: Date): string => {
    try {
      return new Intl.DateTimeFormat(getLocaleFromLanguage(currentLanguage), {
        year: "numeric",
        month: "short",
        day: "numeric",
        hour: "2-digit",
        minute: "2-digit",
      }).format(date)
    } catch {
      return date.toLocaleDateString()
    }
  }

  const formatCurrency = (amount: number, currency = "USD"): string => {
    try {
      return new Intl.NumberFormat(getLocaleFromLanguage(currentLanguage), {
        style: "currency",
        currency: currency,
      }).format(amount)
    } catch {
      return `${currency} ${amount}`
    }
  }

  const getLocaleFromLanguage = (lang: string): string => {
    const localeMap: Record<string, string> = {
      en: "en-US",
      hi: "hi-IN",
      es: "es-ES",
      fr: "fr-FR",
      de: "de-DE",
      pt: "pt-PT",
      it: "it-IT",
      ja: "ja-JP",
      ko: "ko-KR",
      zh: "zh-CN",
      ar: "ar-SA",
      ru: "ru-RU",
    }
    return localeMap[lang] || "en-US"
  }

  const isRTL = availableLanguages.find((lang) => lang.code === currentLanguage)?.rtl || false

  useEffect(() => {
    const savedLanguage = localStorage.getItem("safetour-language")
    if (savedLanguage && availableLanguages.some((lang) => lang.code === savedLanguage)) {
      setLanguage(savedLanguage)
    } else {
      const browserLang = navigator.language.split("-")[0]
      const supportedLang = availableLanguages.find((lang) => lang.code === browserLang)
      if (supportedLang) {
        setLanguage(supportedLang.code)
      }
    }
  }, [])

  return (
    <LanguageContext.Provider
      value={{
        currentLanguage,
        setLanguage,
        translate,
        availableLanguages,
        isRTL,
        formatNumber,
        formatDate,
        formatCurrency,
      }}
    >
      {children}
    </LanguageContext.Provider>
  )
}

export function useLanguage() {
  const context = useContext(LanguageContext)
  if (context === undefined) {
    throw new Error("useLanguage must be used within a LanguageProvider")
  }
  return context
}
